//java program to find if number is odd or even

class EvenProgram									//declaring class
{
	public static void main(String args[])
	{
		int a=66;									//initialization
		if(a%2==0)									//condition for even
		{
		System.out.println("The no. is even");		//if condition is true
		}
		else
		{
		System.out.println("The no. is odd");		//if condition is false
		}
	}
}